package PaneleMenu.Karnety;

public class Karnet {
    int nrKarnetu;
    int nrKlienta;
    String nazwaKarnetu;
    String dataOd;
    String dataDo;

    public Karnet(int nrKarnetu, int nrKlienta, String nazwaKarnetu, String dataOd, String dataDo) {
        this.nrKarnetu = nrKarnetu;
        this.nrKlienta = nrKlienta;
        this.nazwaKarnetu = nazwaKarnetu;
        this.dataOd = dataOd;
        this.dataDo = dataDo;
    }

}
